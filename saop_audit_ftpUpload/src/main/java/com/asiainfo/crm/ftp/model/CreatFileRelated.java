public class CreatFileRelated {
	private Integer Datasum;
	private Integer threadSum;
	private Integer threadSeq;
	private boolean DealFlag;
	
	public Integer getDatasum() {
		return Datasum;
	}
	
	public void setDatasum(Integer Datasum) {
		this.Datasum = Datasum;
	}
	
	public Integer getThreadSum() {
		return threadSum;
	}
	
	public void setThreadSum(Integer threadSum) {
		this.threadSum = threadSum;
	}
	
	public Integer getThreadSeq() {
		return threadSeq;
	}
	
	public void setThreadSeq(Integer threadSeq) {
		this.threadSeq = threadSeq;
	}
	
	public boolean getDealFlag() {
		return DealFlag;
	}
	
	public void setDealFlag(boolean DealFlag) {
		this.DealFlag = DealFlag;
	}
	
}
